def writeInputFilesForInitialParameters(config: dict):
    for sim in config["simulations"]:
        data = open(config["simulations"][sim]["input"], "r").read()
        for ide in config["identification"]:
            data = data.replace(ide, str(config["identification"][ide]["start"]))

        with open(config["simulations"][sim]["input"].split(".")[0] + "_initial.inp", "w+") as f:
            f.write(data)
